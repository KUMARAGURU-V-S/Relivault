const contractAddress = "0x5FbDB2315678afecb367f032d93F642f64180aa3";

export default function EventHistory() {
  const [events, setEvents] = useState<any[]>([]);

  useEffect(() => {
    const fetchEvents = async () => {
      try {
        const provider = new ethers.BrowserProvider(window.ethereum);
        const contract = new ethers.Contract(contractAddress, contractAbi, provider);

        const eventFilter = { address: contractAddress };
        const logs = await provider.getLogs({ ...eventFilter, fromBlock: 0, toBlock: 'latest' });

        const parsedLogs = logs.map(log => {
          try {
            const parsedLog = contract.interface.parseLog(log as any);
            if (parsedLog) {
                return {
                    name: parsedLog.name,
                    timestamp: new Date().toLocaleString(), // Placeholder, block timestamp not easily available on client
                    args: parsedLog.args
                };
            }
          } catch (e) {
            // Ignore logs that don't match the ABI
          }
          return null;
        }).filter(Boolean);

        setEvents(parsedLogs as any[]);
      } catch (error) {
        console.error('Error fetching events:', error);
      }
    };

    if (window.ethereum) {
        fetchEvents();
    }

  }, []);

  return (
    <Card>
      <CardHeader>
        <CardTitle>Event History</CardTitle>
      </CardHeader>
      <CardContent>
        <Table>
          <TableHeader>
            <TableRow>
              <TableHead>Event</TableHead>
              <TableHead>Timestamp</TableHead>
              <TableHead>Details</TableHead>
            </TableRow>
          </TableHeader>
          <TableBody>
            {events.map((event, index) => (
              <TableRow key={index}>
                <TableCell>{event.name}</TableCell>
                <TableCell>{event.timestamp}</TableCell>
                <TableCell>
                  <pre className="text-xs">{JSON.stringify(event.args, null, 2)}</pre>
                </TableCell>
              </TableRow>
            ))}
          </TableBody>
        </Table>
      </CardContent>
    </Card>
  );
}
